export const swtichCaseCoverImage = (slug: string) => {
  if (slug === 'housing-loan') return '/images/lungshan_loan_9.jpg'
  else if (slug === 'land-loan') return '/images/lungshan_loan_12.jpg'
  else if (slug === 'cosignert-loan') return '/images/lungshan_loan_1.jpeg'
  else if (slug === 'bankcredit-loan') return '/images/lungshan_loan_14.jpg'
  else if (slug === 'vehicle-loan') return '/images/lungshan_loan_11.jpg'
  else if (slug === 'product-loan') return '/images/lungshan_loan_24.jpg'
}
